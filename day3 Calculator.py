print('the addition of', m ,'and',n, 'is ','=',   m+n)

print('the subtraction of', m ,'and',n, 'is ','=',m-n)

print('the division of', m ,'and ',n, 'is ','=',  m/n, 'and remainder will be =',m%n)

print('the multiplication of', m ,'and ',n, 'is ','=',m*n)

print('the double division(//) of', m ,'and',n, 'is','=',m//n ,'and remainder will be = ',m%n)

print('the',m,'raise to power of',n,'is ','=',m**n) 
